// Update helper function to use service.id instead of serviceId
export const getServiceCityMetadata = (slug: string, citySlug: string) => {
  const city = cities.find(c => c.slug === citySlug);
  const service = Object.values(services).find(s => s.slug === slug);
  if (!city || !service) return null;

  return service.getMetadata(city);
};